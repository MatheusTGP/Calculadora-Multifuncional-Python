import __main__
import time
while True:
	print('\n ================[ Calculadora Multifuncional V² ]=====================')
	valor1 = int(input("[ Valor1 ]: "))
	valor2 = int(input("[ Valor2 ]: "))
	time.sleep(1)
	print(" ")
	print('∆ Calculando... ∆')
	print(" ")
# Calcular Resultados pela Máquina
	sun1 = valor1 + valor2
	sun2 = valor1 - valor2
	sun3 = valor1 * valor2
	sun4 = valor1 / valor2
# Executar Resultado
	print('============================[ RESULTADOS ]============================')
	print('\n \n [ {} + {} ] = [ {} ] \n \n [ {} - {} ] = [ {} ] \n \n [ {} × {} ] = [ {} ] \n \n [ {} ÷ {} ] = [ {} ] \n'.format(valor1, valor2, sun1, valor1, valor2, sun2, valor1, valor2, sun3, valor1, valor2, sun4))
	print('\n Versão do código: Oficial')
	print('============================[ REINICIANDO ]============================')
# o Programa executa a calculadora multiplas vezes
